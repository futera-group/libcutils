/* define minimal STO-3G basis set 
 
   b - pointer to basis set struct */
void basis_def_sto3g(struct basis *b) {
  unsigned ic,is,ip,id;
  struct basis_center *c;
  struct basis_shell *s;
  for (ic=0; ic<b->n_centers; ic++) {
    c = b->center+ic;
    if (c->type<1 || c->type>BASIS_STO3G_NMAX)
      msg_error_f("atomic number %d is unsupported in STO3G basis set",1,
       c->type);
    /* contracted shells */
    c->n_shells = basis_def_sto3g_ncshell(c->type);
    c->shell = basis_shell_new(c->n_shells);
    for (is=0; is<c->n_shells; is++) {
      s = c->shell+is;
      /* primitive shells */
      s->type = basis_def_sto3g_stype(c->n_shells,is);
      s->n_prim = 3;
      s->exp = vec_falloc(s->n_prim);
      s->cf1 = vec_falloc(s->n_prim);
      if (s->type==BASIS_SHELL_SP)
        s->cf2 = vec_falloc(s->n_prim);
      for (ip=0; ip<s->n_prim; ip++) {
        id = basis_sto3g_id[c->type-1]+3*is+ip;
        s->exp[ip] = basis_sto3g_exp[id];
        s->cf1[ip] = basis_sto3g_cf1[id];
        if (s->type==BASIS_SHELL_SP)
          s->cf2[ip] = basis_sto3g_cf2[id];
        }
      }
    }
  }

