/* return number of contracted shell for given atomic type
 
   a - the atomic number */
unsigned basis_def_sto3g_ncshell(unsigned a) {
  if (a<1 || a>BASIS_STO3G_NMAX)
    msg_error_f("atomic number %d is unsupported in STO3G basis set",1,a);
  if (a<3)  return(1);
  if (a<11) return(2);
  if (a<19) return(3);
  if (a<21) return(4);
  if (a<37) return(5);
  if (a<39) return(6);
  return(7);
  }

/* return type of the shell 
 
   n - total number of shell on the center
   s - shell ID */
short basis_def_sto3g_stype(unsigned n, unsigned s) {
  if (!s)
    return(BASIS_SHELL_S);
  if (s<4 || (n>5 && s<5))
    return(BASIS_SHELL_SP);
  return(BASIS_SHELL_Dp);
  }

