#include <math.h>

/* -------------------------------------------------------------------------- */

/* evaluate normalized cauchy function
 
   m - mean value
   s - sigma
   x - functional value */
double math_cauchy_fce(double m, double s, double x) {
  return((s*s)/((x-m)*(x-m)+(s*s))/(M_PI*s));
  }

