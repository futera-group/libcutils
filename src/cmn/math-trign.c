#include <math.h>

/* -------------------------------------------------------------------------- */

/* calculate arc cosine (argument truncation to [-1,+1] interval)
 
   c - argument of the function */
double math_acos(double c) {
  if (c<=-1.0)
    return(M_PI);
  if (1.0<=c)
    return(0.0);
  return(acos(c));
  }

