/* convert atomic number to atomic mass */
double atom_mass(unsigned num) {
  double m = 0.0;
  if (num>=1 && num<ATOM_MAX_NUM)
    m = atom_masses[num-1];
  return(m);
  }

/* convert atom name to atomic mass */
double atom_mass_name(char *name) {
  unsigned n = atom_num(name);
  if (n>0)
    return(atom_masses[n-1]);
  return(0.0);
  }

/* convert PDB atom name to atomic mass */
double atom_mass_name_pdb(char *name) {
  unsigned n = atom_num_pdb(name);
  if (n>0)
    return(atom_masses[n-1]);
  return(0.0);
  }

